"""
charts.py  —  Song definitions and chart patterns for the rhythm game.

To add a new song:
  1. Drop the MP3 into the game folder
  2. Run:  python3 make_chart.py YourSong.mp3
     This auto-generates a SongDef block and appends it here.
  3. Import it in SONGS at the bottom of this file.

Pattern format:  (time_seconds, lane_0_to_3, is_hold_bool, hold_duration_sec)
"""

import os, random

_DIR = os.path.dirname(os.path.abspath(__file__))

def _audio(filename):
    """Returns full path to an audio file, or None if missing."""
    p = os.path.join(_DIR, filename)
    return p if os.path.exists(p) else None


class SongDef:
    def __init__(self, title, artist, bpm, pattern, audio_file=None):
        self.title      = title
        self.artist     = artist
        self.bpm        = bpm
        self.pattern    = pattern
        self.audio_file = audio_file


def _gen_random(duration=60, bpm=120, seed=0):
    """Fallback random pattern generator for songs without a real chart."""
    rng  = random.Random(seed)
    beat = 60 / bpm
    events, t = [], 1.0
    prev = []
    while t < duration:
        lanes = rng.sample(range(4), rng.choices([1,2], weights=[70,30])[0])
        for lane in lanes:
            is_hold  = rng.random() < 0.15
            hold_dur = round(rng.uniform(0.4, 0.9), 2) if is_hold else 0.0
            events.append((round(t,3), lane, is_hold, hold_dur))
        t += beat * rng.choice([0.5,1,1,1,1.5,2])
    return events


